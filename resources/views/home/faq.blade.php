                    <div id="collapseFive" class="panel-collapse collapse">
                        <div class="panel-body">
                            <p><h4> 右舵左行 – 汽车必须沿公路左侧行驶，车上的驾驶员座椅设在右侧。</h4><br>
                                在新西兰，任何时候都必须沿公路左侧行驶。如果您习惯于右侧行车，要记住这一点可能并不容易，尤其是刚刚起步上路时。谨记——如果您在开车，则必须坐在路中间一侧——而坐在副驾驶位置的乘客应位于路边侧。
                            <h4>由于在新西兰开车与您以前所习惯的开车方式会有很大的不同，因此必须时刻保持警觉，并注意要休息充分。</h4><br>
                                疲劳驾驶是一件极其危险的事情，尤其是在刚到一个新的国家，对当地的公路和交通规则还不熟悉的情况下。来到新西兰的游客之所以可能更容易感到疲惫，主要是由于时差原因，起得早，睡得晚，或是由于前一天开了较长时间的车。请确保自己头脑清醒，保持警惕——在公路上疲劳驾驶会带来安全隐患！
                            <h4>公路的路况通常各有不同，有些路狭窄弯曲，甚至可能需要翻山越岭。</h4><br>
                                新西兰地形的多样性意味着这里的公路通常较窄，常有山路，弯路，而且多急转弯。离开大城市之后，郊外宽阔的公路不多。这里的公路多为双向两车道，中间没有隔离带。此外，您还有可能会遇到碎石路。如果有车辆想从后面超车时，要注意留出足够的时间，减速行驶或找个安全的地方靠边停车。确保充足的休息，以保持清醒的头脑和充沛的精力。
                            <h4> 只看地图时，常常会低估开车所需的时间。</h4><br>
                                地图上可能不会显示道路的狭窄、弯曲程度。一段看似很短的路程可能需要开很长时间。例如：从霍基蒂卡（Hokitika）到哈斯特镇（Haast）是备受游客青睐的一条自驾线路，可停车观看新西兰的冰川，地图上显示这段路程为 278 公里（172 英里），看上去可能只是一段 3 小时的短途旅行。但由于这段路弯弯曲曲，驾驶员至少应当预留 4 小时的自驾时间。这种情况在新西兰非常普遍——因此，预留的时间应当始终大于您预计所需的时间。
                            <h4>  与天气相关的隐患非常常见。</h4><br>
                                在新西兰，您有可能会在一天之内经历四季变化。出发时可能还是万里晴空、阳光明媚，到达目的地时可能就会大雨倾盆、冰雹交加。正因如此，公路上随时都有可能出现与天气相关的隐患。为此，出发之前务必要查看天气预报，然后对行程计划进行适当的调整。如果您是在冬季、春季或秋末开车游览南岛，那么很有可能会遇到下雪天气——如果天气预报有寒流，则必须随车携带防滑链。大多数的车辆租赁公司都会为您提供防滑链，并向您演示具体的安装方法。请阅读我们的冬季自驾小贴士。
                            <h4>    冬天的路况可能会变化莫测。</h4><br>
