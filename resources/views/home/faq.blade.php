                                不过，碎石路面也有它的好处，新西兰每年雨水较多，在这种路面上潮湿天气对于汽车抓地力的影响较小，能够缩短紧急情况下刹车的距离，所以新西兰境内最常见的公路就是用的chipseal路面。这种路面的铺设成本低，施工工期短，寿命比沥青路面长，后期维护次数也可以相应的减少；可以说是最适合新西兰国情的道路。
                                碎石路面（未封）Unsealed surface
                                如果您去一些游人较少、山高路远的地方，您可能会碰到碎石路面，英文名称Unsealed surface。这是比较难开的一种路面，在路基之上就铺设了一层小小的碎石子而已，车轮行驶在上面，由于小石子的存在不会对路基产生直接的压力造成损坏，但是小石子存在于车轮与道路之间会严重降低抓地力，所以您务必要按照新西兰道路交规roadcode的要求，以不超过30公里的速度在上面行驶。因为高速行驶的车辆在碎石路面上不仅直线刹车距离会增长，在拐弯的时候也可能由于侧向力过大造成轮胎的横向移动，这很危险，因为这种路面通常都在山高路险的地方，驾驶的不谨慎会产生非常严重的后果。
                                另外，干燥的天气下这种路面行驶会在车后扬起很大的灰尘，让后车视线不清；而后车轮也会把路面上的小石子扬起来，如果您在这种路面上跟车，请务必打开前大灯、远离前车，让您的视线变得清晰且确保您的挡风玻璃不会被前车带起来的小石头击中产生裂纹。
