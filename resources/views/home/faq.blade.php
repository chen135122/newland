                                众所周知，中餐馆原材料品种繁杂，调料味道鲜明，厨房较为油腻；这一点与西餐厅的厨房相比有着先天的不足，厨房更容易“招惹”蟑螂老鼠之类的有害生物光临并入住；所以，中餐馆一直是奥克兰地区议会“重点照顾”的对象。
                                奥克兰市议会对所有餐饮服务业场所，按照情况和卫生级别颁发“卫生等级证明”，它们是：<br>
                                A，非常卫生<br>
                                B，卫生情况良好<br>
                                D，卫生情况不好<br>
                                E，卫生情况糟糕<br>
                                Pending，刚申请评级的餐馆或是刚刚转手过的餐馆，等待评级
                                也许您会问，为什么没有C这个级别？其实原因很简单，奥克兰市议会认为，没有Average、也就是“平均”这个卫生概念，如果好，就是A或B；如果不好，就是D或E。这也就避免了负责食品卫生监督的工作人员，搞“人情牌”“平均主义”这种事情；这也能够从另一方面体现出西方社会的一些办事作风吧。如果一个餐馆悬挂A/B，那么代表您可以在里面放心的进行消费；如果一个餐馆是D，那么您需要小心；如果是E，请您按照自身的健康情况作出决定吧：）；如果没有这个证明，那么您可以要求经营者出示他们的评级，到底是多少，有些餐饮业者会因为自身的卫生评级低而故意隐藏这个证明。
                                那么这个卫生评级的分数，是如何被计算的呢？或者说，怎么做才能让市议会的检查人员满意呢？主要由以下几个方面
                                环境卫生，包括餐饮区和操作区，都要洁净<br>
                                垃圾的处理空间、以及垃圾的处理方式，还有户外的垃圾堆放地的洁净程度<br>
                                食品展示柜、或是食品存储地的卫生（例如冰柜、冰箱等等）<br>
                                处理食物的操作流程是否洁净<br>
                                是否有外来生物？例如，蟑螂、老鼠、各种“寻味而来”的不速之客，以及它们的排泄物<br>
                                操作间是否有食物的异味飘出？<br>
                                是否有过期食品、不安全食品被存储或是销售？<br>
                                上一次评级的证书，是否被悬挂在最显眼的地方？<br>
                                人员是否都经过了Food Safety方面的培训？
                            </p>
