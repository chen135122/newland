                            <p>新西兰的旅游旺季各个景点都熙熙攘攘，而由世界各地、尤其是从冬季的北半球前来这里度假的旅客把每个航班也都坐得满满当当。在家千日好、出门一日难，作为旅客除了要安排好旅游行程之外，一份旅游保险也是必不可少的。不少华人朋友远道从中国大陆而来，可以掏几万人民币买机票、住好的酒店、乘坐好的汽车，却舍不得购买几百人民币的旅游保险；诚然新西兰是一个相对安全的旅游目的地，并且任何意外人身伤害都有意外伤害赔偿局ACC来理赔（不分本地居民还是外来游客）。但是万一旅客在新西兰生病了呢？万一行李丢失、被盗了呢？万一航班延误造成了行程延误呢？这时候一份旅游保险可以让旅客的财产损失降到最低，亦或是不必担心新西兰高昂的医疗费用（新西兰的医疗费用可以用“天价”来形容）。本文不是软性广告，只是在旅游旺季来临之前，希望更多的大陆游客能够看到这篇文章，在动身之前不要“心疼小钱”，买一份旅游保险为自己的旅程加一层保护。至于购买哪一家的服务那就随您喜欢了，中国大陆有很多家保险公司，有不少与国际知名跨国保险公司都有合作关系，任选一家性价比高、服务面广的就好；到新西兰落地后购买本地保险公司的旅游保险好不好呢？答案是不好，因为第一是价格更贵、第二是语言不通很多条款您无法理 解、第三是从您出发到本地的保险生效将会有时间差造成的“保险空窗期”。</p>
