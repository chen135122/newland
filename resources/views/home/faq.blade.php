                            <p> 中国大陆->日本->奥克兰，多家航空公司从中国大陆出发的航班前往日本东京，然后选择日航/纽航前往奥克兰</p>
                            <p> 如果您在中国大陆的出发地不是北京、上海、广州的话，您可以选择航班前往这三个城市然后联程飞往奥克兰；从华东地区出发的朋友则可以选择从上海启程，纽航、东航都是很好的选择，票价也差不多；华南、西南地区的朋友自然是选择从广州出发的南方航空公司的航班了，班次很多，并且票价非常有竞争力；从中国北方出发的朋友则可以选择北京、或是广州作为转机航站，然后继续前往新西兰。</p>
                            <p>如果选择从香港转机，实话说，无论是纽航还是国泰的票价都属于较高的；但是在香港转机有一个优势，就是香港机场的物价非常便宜，且可以购买的东西很多，而且在转机过程中从机场购买的东西不计算入行李的重量；通常一些喜欢购物的朋友会选择多掏一点钱从香港转机，然后留出4-5个小时在机场购物、吃饭，再登上下一个航班前往奥克兰。</p>
                            <p>从新加坡、吉隆坡、日本和韩国转机，目前已经不再是华人朋友们的主流选择，因为这些航空公司提供的票价并没有什么竞争力，而且这四个地方转机都会多“绕”一点路程；除非这些联程航线推出了非常便宜的特价机票，或者您希望在这几个地方路过的时候先玩一下再继续前往新西兰，才值得考虑。</p>
