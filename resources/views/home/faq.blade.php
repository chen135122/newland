                                沥青路面在新西兰的大城市和城市周边最普遍，黑黝黝的路面比较平滑，道路承载能力强，汽车行驶在上面噪声较低，对于汽车轮胎的磨损也较低，路面对于汽车的反馈非常清晰，驾驶感觉最好。天气晴好路面干燥的时候，沥青路面可以提供良好的整体抓地力；但是当下雨或是路面有雾气露水的时候，防滑性会大幅度降低；所以，天气不好的时候注意在沥青路面上要小心驾驶，增加跟车距离，弯道的时候提前减速。
                                碎石封层路面Chip-seal
                                碎石封层路面英文名Chipseal / Chip-seal，这种路面在中国大陆不常见，是一种较为廉价且耐用的路面铺设方式，在新西兰有很多开放式道路、居民区道路、甚至是高速公路，都使用这种路面。路面上，就是用沥青混合一层小小的碎石，在路基上直接铺设，比沥青路面要薄，也没有在表层使用“细料”，所以给人的感觉是粗粗的，光脚走在上面会感觉有点“硌脚”。
